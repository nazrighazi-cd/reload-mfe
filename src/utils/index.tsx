export function exNameEmail(data) {
  let {subscriberRecord, csInfo} = data || ({} as any);
  // const dguardDown = checkFeature('dGaurd', true); //MYDIGI3-2428 hide csInfo name and email if dguard is down

  const {dguardInfo} = subscriberRecord || ({} as any);

  let {
    email,
    fullName,
    dguardId: dguardid,
    username,
  } = dguardInfo || ({} as any);

  let {accountEmail} = subscriberRecord || ({} as any);
  const exclude = ['dummy@digi.com', 'dummy@digi.com.my'];

  if (exclude.find(key => key === accountEmail)) accountEmail = null;

  if (isEmpty(dguardInfo)) {
    csInfo = (csInfo && csInfo[0]) || ({} as any);
    const {dguardid: did, email: csEmail, name: csName} = csInfo || ({} as any);

    dguardid = did;

    //MYDIGI3-2428 hide csInfo name and email if dguard is down
    // if (isEmpty(fullName) && !dguardDown) fullName = csName;
    // if (isEmpty(email) && !dguardDown) email = csEmail;
    // if (isEmpty(email)) email = accountEmail;
    if (isEmpty(fullName)) fullName = csName;
    if (isEmpty(email)) email = csEmail;
    if (isEmpty(email)) email = accountEmail;

    if (isEmpty(fullName)) {
      const {customerInfo} = subscriberRecord || ({} as any);
      const {CustomerDetails} = customerInfo || ({} as any);
      const {Name: accountName} = CustomerDetails || ({} as any);
      fullName = accountName;
    }
  }
  return {
    email: email,
    fullName: fullName,
    dguardid: dguardid,
    username: username,
  };
}

export function encrypt(content, publicKey) {
  const crypt = new JSEncrypt({default_key_size: 2056});
  crypt.setKey(publicKey);
  return crypt.encrypt(content);
}

export function createPublicKey(keyPart1, keyPart2) {
  let startPart = '-----BEGIN PUBLIC KEY-----';
  return startPart.concat(keyPart1, keyPart2, '-----END PUBLIC KEY-----');
}

export function formatUrl(url) {
  const objUrl = new URL(url, true);
  let {pathname, query, hostname} = objUrl || ({} as any);
  try {
    pathname = !!pathname ? pathname.split('&')[0] : pathname;
  } catch (er) {}

  const arrPaths = pathname ? R.filter(e => !!e, pathname.split('/')) : [];

  return {
    query,
    paths: arrPaths,
    pathname,
    hostname,
  };
}

